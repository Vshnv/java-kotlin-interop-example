package com.github.vshnv.interop;

public class JHuman {
    private final String name;
    private final int age;

    public JHuman(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
